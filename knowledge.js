/**
 * Нормализация строки для сравнения: нижний регистр, без лишних пробелов.
 */
function normalize(s) {
  return (s || '').toLowerCase().replace(/\s+/g, ' ').trim();
}

/**
 * Подбор ответа по тексту вопроса. Возвращает первый подходящий ответ или null.
 */
/**
 * Жёстко закреплённые ответы (выше RAG), чтобы LLM не «додумывала» списки.
 */
function getPinnedAnswer(userMessage) {
  const text = normalize(userMessage);
  if (!text) return null;

  const isProtocolsQuick = /^протоколы?$/.test(text) || /^protocols?$/.test(text);
  const isGeneralProtocolsQuestion =
    text.includes('протокол') &&
    (text.includes('какие') ||
      text.includes('список') ||
      text.includes('поддержива') ||
      text.includes('есть ли') ||
      text.includes('перечисл'));

  if (isProtocolsQuick || isGeneralProtocolsQuestion) {
    return PROTOCOLS_OFFICIAL_ANSWER;
  }

  // Кнопка «Модельный ряд» и короткие вопросы — иначе RAG часто отвечает «в документах нет»
  const isModelRangeQuick = text === 'модельный ряд' || /^модельный ряд[.!?…]*$/.test(text);
  const isShortModelRangeQuestion =
    text.includes('модельный ряд') &&
    text.length <= 80 &&
    !/[A-Za-zА-Яа-я]{2,}\d{2,}/.test(userMessage);
  if (isModelRangeQuick || isShortModelRangeQuestion) {
    return MODEL_RANGE_OFFICIAL_ANSWER;
  }

  return null;
}

function getAnswer(userMessage) {
  const text = normalize(userMessage);
  if (!text) return null;

  for (const block of knowledge) {
    const found = block.keywords.some(kw => text.includes(normalize(kw)));
    if (found) return block.answer;
  }
  return null;
}

